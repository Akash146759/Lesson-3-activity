#Calculating circumference of a circle
radius=float(input("Enter radius:"))

#definition of function
def circle_circumference(radius):   
  return 2*3.14*radius #2_pi_r where 2 * 3.14 * r
  
print(circle_circumference(radius))#here you pass the value of r you have to pass a number 

print(circle_circumference(9))

print(circle_circumference(3))




